void printBuf( uint8_t *buf, size_t buf_len ) {
    for (size_t i = 0; i < buf_len; i++) {
        printf("%02hhx ", buf[i]);
        if ( (i+1) % 16 == 0)
            printf("\n");
        else if ( (i+1) % 8 == 0 )
            printf(" ");
    }
    printf("\n");
}

